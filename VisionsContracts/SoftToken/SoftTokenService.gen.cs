    public partial class SoftTokenService: SoftTokenServiceBase
    {
        public static Task<TransactionReceipt> DeployContractAndWaitForReceiptAsync(Nethereum.Web3.IWeb3 web3, SoftTokenDeployment softTokenDeployment, CancellationTokenSource cancellationTokenSource = null)
        {
            return web3.Eth.GetContractDeploymentHandler<SoftTokenDeployment>().SendRequestAndWaitForReceiptAsync(softTokenDeployment, cancellationTokenSource);
        }

        public static Task<string> DeployContractAsync(Nethereum.Web3.IWeb3 web3, SoftTokenDeployment softTokenDeployment)
        {
            return web3.Eth.GetContractDeploymentHandler<SoftTokenDeployment>().SendRequestAsync(softTokenDeployment);
        }

        public static async Task<SoftTokenService> DeployContractAndGetServiceAsync(Nethereum.Web3.IWeb3 web3, SoftTokenDeployment softTokenDeployment, CancellationTokenSource cancellationTokenSource = null)
        {
            var receipt = await DeployContractAndWaitForReceiptAsync(web3, softTokenDeployment, cancellationTokenSource);
            return new SoftTokenService(web3, receipt.ContractAddress);
        }

        public SoftTokenService(Nethereum.Web3.IWeb3 web3, string contractAddress) : base(web3, contractAddress)
        {
        }

    }


